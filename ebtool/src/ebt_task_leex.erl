-module(ebt_task_leex).

-export([perform/3]).

perform(_Target, Dir, Config) ->
    Files = filelib:wildcard(Dir ++ "/src/*.xrl"),
    case xl_lists:eforeach(fun(F) ->
        io:format("generate lexer ~s~n", [F]),
        leex:file(F)
    end, Files) of
        ok -> {ok, Config};
        E -> E
    end.

